fun main(args: Array<String>) {

        var myNumber : Int = 77
        var myDouble : Double = 5.99
        var myFloat : Float = 5.99f
        var myChar : Char = 'N'
        var myBoolean : Boolean = true
        var myText : String = "Hello World"
        var myLong : Long = myNumber.toLong()

        println(myNumber)
        println(myDouble)
        println(myFloat)
        println(myChar)
        println(myBoolean)
        println(myText)
        println(myLong)
    }